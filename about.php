<section id="about">
    <h2>Über das Projekt</h2>

    <p>
    Das Projekt entstand 2022 in einem Bemühen meinerseits, nicht nur meine bisher in verschiedenen Programmen und zu oft Dokumentdateinamen erfassten genealogischen Daten in einem System zu bündeln und für mich selbst übersichtlicher zu gestalten, sondern diese Daten darüber hinaus auch für andere zugänglich zu machen.
    </p>

    <p>
    In meiner mehrjährigen Erfahrung mit genealogischer Forschung hat sich als großes Problem für mich vor allem die schlechte Durchsuch- und Auffindbarkeit von interessanten Personenstandsdaten herauskristallisiert. Kirchenbucheinträge und Standesamtunterlagen müssen erst im richtigen Archiv (oder noch schlimmer auf inzwischen umkonvertierten polnischen Kirchendachböden) ausfindig gemacht werden, dann sind diese bisher noch selten als Scans von anderswo als dem Archiv selbst verfügbar und selbst wenn die Unterlagen vor einem liegen (ob physisch oder digital), blättert man oft Stunden und Tage ganze Jahrzehnte an Eintragungen durch, um eine einzelne relevante Erwähnung zu finden. Und das schlimmste daran: Meistens ist man nicht die*der Erste, die diese Dokumente danach durchsieht.
    </p>

    <p>
    Jahre von akkumulierter Arbeit könnten gespart werden, wenn die Erfassung jedes*r Einzelnen*r anderen zur Verfügung stünde. Vielfach passiert dies online schon und so lassen sich oft Daten finden, auf die man sonst nie gestoßen wäre, weil man nicht darauf gekommen wäre, an diesem Ort zu suchen. Viele dunkle Punkte in der eigenen Forschung lassen sich so nun im digitalen Zeitalter überwinden, wo dies vorher nicht möglich war. Leider konzentrieren sich aber viele dieser Seiten nur auf eine sehr spezifisch eingegrenzte Regionen oder Zeiträume und sind in ihren Suchmöglichkeiten sehr eingeschränkt.
    </p>

    <p>
        Die <b>Genealogische Datenbank</b> ist daher von mir auf Basis meiner Erfahrung mit sowohl den historischen Quellen und ihren Inhalten als auch meinem Frust mit so mancher anderer Sammelseite entwickelt worden und dafür ausgelegt, viele Informationen zu erfassen, die anderswo nicht Berücksichtigung finden.
    </p>

    <h3>Zukuntspläne</h3>

    <p>
        Neben der langfristigen Dateneingabe meiner vorhandenen Datensätze, die viel Zeit beansprucht, weshalb die Anzahl der erfassten Einträge nur graduell und langsam wächst, sieht die Zukunft vor, den Suchalgorithmus einen besseren Abgleich mit ähnlich lautenden Namen hinzuzufügen, welche nicht von der Substringsuchmöglichkeit erfasst werden.
    </p>

    <p>
        Weitere Pläne umfassen eine separate Liste der erfassten Kirchspiele und Standesämter und der Anzahl ihrer Einträge sowie Informationen dazu, ob Jahrgänge vollständig übertragen wurden, sowie eine bessere Georeferenzierung und Namensvariantenberücksichtigung der Ereignisorte, da diese historisch häufig mehrere Namenswechsel durchlebt haben oder sich Namen in der Umgebung doppeln, und eine Erfassung der aktuellen Quellenstandorte, auf die sich hier bezogen wird.
    </p>

